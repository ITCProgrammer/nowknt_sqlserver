/*
Run this on SQL Server to prepare telescope monitor in DB dbknitt
with dedicated schema observability.
*/

USE [dbknitt];
GO

IF NOT EXISTS (SELECT 1 FROM sys.schemas WHERE name = N'observability')
BEGIN
    EXEC('CREATE SCHEMA [observability]');
END;
GO

/*
If table exists in dbo and not yet in observability, move it.
*/
IF OBJECT_ID(N'[dbo].[tbl_debug_monitor]', N'U') IS NOT NULL
   AND OBJECT_ID(N'[observability].[tbl_debug_monitor]', N'U') IS NULL
BEGIN
    ALTER SCHEMA [observability] TRANSFER [dbo].[tbl_debug_monitor];
END;
GO

IF OBJECT_ID(N'[observability].[tbl_debug_monitor]', N'U') IS NULL
BEGIN
    CREATE TABLE [observability].[tbl_debug_monitor] (
        [id] BIGINT IDENTITY(1,1) NOT NULL PRIMARY KEY,
        [created_at] DATETIME2 NOT NULL DEFAULT SYSDATETIME(),
        [log_type] VARCHAR(20) NOT NULL,
        [request_id] VARCHAR(40) NULL,
        [method] VARCHAR(10) NULL,
        [path] NVARCHAR(500) NULL,
        [query_string] NVARCHAR(MAX) NULL,
        [status_code] INT NULL,
        [duration_ms] INT NULL,
        [ip_address] VARCHAR(45) NULL,
        [user_name] NVARCHAR(100) NULL,
        [session_user_id] INT NULL,
        [is_ajax] BIT NOT NULL DEFAULT(0),
        [payload_json] NVARCHAR(MAX) NULL,
        [error_type] VARCHAR(50) NULL,
        [error_message] NVARCHAR(MAX) NULL,
        [error_file] NVARCHAR(300) NULL,
        [error_line] INT NULL,
        [error_trace] NVARCHAR(MAX) NULL,
        [memory_peak_kb] INT NULL,
        [user_agent] NVARCHAR(400) NULL
    );
END;
GO

IF NOT EXISTS (
    SELECT 1
    FROM sys.indexes
    WHERE object_id = OBJECT_ID(N'[observability].[tbl_debug_monitor]', N'U')
      AND name = N'IX_tbl_debug_monitor_created_at'
)
BEGIN
    CREATE INDEX [IX_tbl_debug_monitor_created_at]
        ON [observability].[tbl_debug_monitor]([created_at] DESC);
END;
GO

IF NOT EXISTS (
    SELECT 1
    FROM sys.indexes
    WHERE object_id = OBJECT_ID(N'[observability].[tbl_debug_monitor]', N'U')
      AND name = N'IX_tbl_debug_monitor_log_type'
)
BEGIN
    CREATE INDEX [IX_tbl_debug_monitor_log_type]
        ON [observability].[tbl_debug_monitor]([log_type]);
END;
GO
